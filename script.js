function animation() {
    document.getElementsByClassName('first-part')[0].style.transform = "translateX(0)" ;
    document.getElementsByClassName('img')[0].style.opacity = 1 ;
    document.getElementsByClassName('from-part')[0].style.transform = "translateX(0)" ;
    document.getElementsByClassName('to-part')[0].style.transform = "translateX(0)" ;
}
animation();